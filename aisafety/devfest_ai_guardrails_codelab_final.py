# # Challenge 5: Solution - Advanced Tool Output Filtering with Selective Redaction
#
import re
#
class SelectiveRedactionPlugin(base_plugin.BasePlugin):
    """Plugin that redacts only unsafe portions of tool outputs."""
#
    def __init__(self, judge_agent: llm_agent.LlmAgent):
        super().__init__(name="selective_redaction")
        self.judge_agent = judge_agent
        self.judge_runner = runners.InMemoryRunner(
            agent=judge_agent,
            app_name="redaction_judge"
        )
        print("✂️  Selective redaction plugin initialized")
#
    async def _identify_unsafe_sentences(self, text: str) -> List[int]:
        """Identify which sentences are unsafe (returns list of sentence indices)."""
#         # Split into sentences
        sentences = re.split(r'(?<=[.!?])\s+', text)
#
        unsafe_indices = []
#
        for i, sentence in enumerate(sentences):
#             # Check each sentence individually
            wrapped = f"<tool_output>\n{sentence}\n</tool_output>"
#
            response, _ = await chat_with_agent(
                self.judge_agent,
                self.judge_runner,
                wrapped
            )
#
            if "UNSAFE" in response.upper():
                unsafe_indices.append(i)
#
        return unsafe_indices
#
    def _redact_sentences(self, text: str, unsafe_indices: List[int]) -> str:
        """Redact specific sentences while keeping safe content."""
        sentences = re.split(r'(?<=[.!?])\s+', text)
#
        redacted = []
        for i, sentence in enumerate(sentences):
            if i in unsafe_indices:
                redacted.append("[REDACTED FOR SAFETY]")
            else:
                redacted.append(sentence)
#
        return " ".join(redacted)
#
    async def after_tool_callback(
        self,
        tool: base_tool.BaseTool,
        tool_args: dict,
        tool_context: tool_context.ToolContext,
        result: dict
    ) -> dict | None:
        """Selectively redact unsafe portions of tool output."""
        result_str = str(result)
#
#         # First, quick check if entire output is safe
        wrapped = f"<tool_output>\n{result_str}\n</tool_output>"
        response, _ = await chat_with_agent(
            self.judge_agent,
            self.judge_runner,
            wrapped
        )
#
#         # If entirely safe, pass through
        if "SAFE" in response.upper():
            return None
#
        print(f"⚠️  Unsafe content detected in tool '{tool.name}' output")
        print("   🔍 Performing selective redaction...")
#
#         # Identify which sentences are unsafe
        unsafe_indices = await self._identify_unsafe_sentences(result_str)
#
        if unsafe_indices:
#             # Redact only unsafe sentences
            redacted_result = self._redact_sentences(result_str, unsafe_indices)
#
            print(f"   ✂️  Redacted {len(unsafe_indices)} unsafe sentence(s)")
            print(f"   ℹ️  Note: Partial content was filtered for safety")
#
            return {
                "result": redacted_result,
                "warning": f"⚠️ Some content was redacted for safety. {len(unsafe_indices)} unsafe portion(s) removed."
            }
#
#         # If we can't identify specific sentences, block entire output
        print("   🚫 Unable to isolate unsafe content, blocking entire output")
        return {"error": "Tool output blocked by safety filter (unable to selectively redact)"}
