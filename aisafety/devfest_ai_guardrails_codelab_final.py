from collections import defaultdict
from datetime import datetime, timedelta

class RateLimitPlugin(base_plugin.BasePlugin):
    """Plugin to prevent abuse by rate limiting user messages."""

    def __init__(self, max_messages=10, window_seconds=60):
        super().__init__(name="rate_limit")
        self.max_messages = max_messages
        self.window_seconds = window_seconds
        # Track messages per user: {user_id: [timestamp1, timestamp2, ...]}
        self.user_messages = defaultdict(list)
        print(f"⏱️  Rate limit plugin initialized: {max_messages} messages per {window_seconds}s")

    def _clean_old_messages(self, user_id: str):
        """Remove message timestamps older than the time window."""
        cutoff_time = datetime.now() - timedelta(seconds=self.window_seconds)
        self.user_messages[user_id] = [
            ts for ts in self.user_messages[user_id]
            if ts > cutoff_time
        ]

    def _is_rate_limited(self, user_id: str) -> bool:
        """Check if user has exceeded rate limit."""
        self._clean_old_messages(user_id)
        return len(self.user_messages[user_id]) >= self.max_messages

    def _record_message(self, user_id: str):
        """Record a new message timestamp for user."""
        self.user_messages[user_id].append(datetime.now())

    async def on_user_message_callback(
        self,
        invocation_context: invocation_context.InvocationContext,
        user_message: types.Content
    ) -> types.Content | None:
        """Check rate limit before processing message."""
        user_id = invocation_context.session.user_id

        if self._is_rate_limited(user_id):
            print(f"🚫 Rate limit exceeded for user: {user_id}")
            # Set flag to block execution
            invocation_context.session.state["rate_limited"] = True

            # Calculate wait time
            oldest_msg = min(self.user_messages[user_id])
            wait_seconds = int((oldest_msg + timedelta(seconds=self.window_seconds) - datetime.now()).total_seconds())
            invocation_context.session.state["wait_seconds"] = max(wait_seconds, 1)

            return types.Content(
                role="user",
                parts=[types.Part.from_text(text="[Rate limited]")]
            )

        # Record this message
        self._record_message(user_id)
        return None

    async def before_run_callback(
        self,
        invocation_context: invocation_context.InvocationContext
    ) -> types.Content | None:
        """Return rate limit message if user is rate limited."""
        if invocation_context.session.state.get("rate_limited", False):
            wait_seconds = invocation_context.session.state.get("wait_seconds", 60)
            # Reset flag
            invocation_context.session.state["rate_limited"] = False

            return types.Content(
                role="model",
                parts=[types.Part.from_text(
                    text=f"⏱️ Rate limit exceeded. You've sent too many messages too quickly. "
                         f"Please wait {wait_seconds} seconds before trying again."
                )]
            )
        return None



