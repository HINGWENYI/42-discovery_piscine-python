class SafetyMetricsPlugin(base_plugin.BasePlugin):
    """Plugin to track and report safety metrics."""
#
    def __init__(self):
        super().__init__(name="safety_metrics")
        # Metrics storage
        self.metrics = {
            "total_messages": 0,
            "blocked_messages": 0,
            "allowed_messages": 0,
            "blocks_by_category": Counter(),  # user_message, tool_output, model_output
            "blocks_by_user": Counter(),
            "block_history": [],  # List of {timestamp, user_id, category, content_hash}
        }
        print("📊 Safety metrics plugin initialized")
#
    async def on_user_message_callback(
        self,
        invocation_context: invocation_context.InvocationContext,
        user_message: types.Content
    ) -> types.Content | None:
        """Track user message metrics."""
        self.metrics["total_messages"] += 1
        return None
#
    async def before_run_callback(
        self,
        invocation_context: invocation_context.InvocationContext
    ) -> types.Content | None:
        """Check if message was blocked by another plugin."""
#         # If another plugin set this flag, we record it as a block
        if not invocation_context.session.state.get("is_user_prompt_safe", True):
            self._record_block(
                user_id=invocation_context.session.user_id,
                category="user_message",
                content_hash=hash(invocation_context.session.state.get("last_message", ""))
            )
        return None
#
    async def after_tool_callback(
        self,
        tool: base_tool.BaseTool,
        tool_args: dict,
        tool_context: tool_context.ToolContext,
        result: dict
    ) -> dict | None:
        """Track tool output blocks."""
#         # Check if result is an error from safety filter
        if isinstance(result, dict) and "error" in result:
            if "blocked" in str(result["error"]).lower():
                self._record_block(
                    user_id="system",
                    category="tool_output",
                    content_hash=hash(str(result))
                )
        return None
#
    async def after_model_callback(
        self,
        callback_context: base_plugin.CallbackContext,
        llm_response: llm_response.LlmResponse
    ) -> llm_response.LlmResponse | None:
        """Track model output metrics."""
#         # This won't detect blocks since they happen before we get here
#         # But we can count successful responses
        if llm_response.content:
            self.metrics["allowed_messages"] += 1
        return None
#
    def _record_block(self, user_id: str, category: str, content_hash: int):
        """Record a safety block event."""
        self.metrics["blocked_messages"] += 1
        self.metrics["blocks_by_category"][category] += 1
        self.metrics["blocks_by_user"][user_id] += 1
#
        self.metrics["block_history"].append({
            "timestamp": datetime.now().isoformat(),
            "user_id": user_id,
            "category": category,
            "content_hash": content_hash
        })
#
    def get_metrics_summary(self) -> str:
        """Generate a formatted metrics summary."""
        m = self.metrics
#
        summary = f"""
 📊 SAFETY METRICS DASHBOARD
 {'='*60}

 OVERVIEW:
   Total Messages:       {m['total_messages']}
   Blocked Messages:     {m['blocked_messages']}
   Allowed Messages:     {m['allowed_messages']}
   Block Rate:           {(m['blocked_messages']/max(m['total_messages'], 1)*100):.1f}%

 BLOCKS BY CATEGORY:
 """
        for category, count in m["blocks_by_category"].most_common():
            summary += f"  {category:20s}: {count}\n"
#
        summary += "\nTOP USERS BY BLOCKS:\n"
        for user_id, count in m["blocks_by_user"].most_common(5):
            summary += f"  {user_id:20s}: {count}\n"
#
        summary += f"\nRECENT BLOCKS (last 5):\n"
        for event in m["block_history"][-5:]:
            summary += f"  {event['timestamp']} | {event['category']} | User: {event['user_id']}\n"
#
        summary += "="*60
        return summary
#
    def export_metrics(self) -> Dict:
        """Export raw metrics for external analysis."""
        return {
            "summary": {
                "total_messages": self.metrics["total_messages"],
                "blocked_messages": self.metrics["blocked_messages"],
                "allowed_messages": self.metrics["allowed_messages"],
                "block_rate": self.metrics["blocked_messages"] / max(self.metrics["total_messages"], 1)
            },
            "categories": dict(self.metrics["blocks_by_category"]),
            "users": dict(self.metrics["blocks_by_user"]),
            "history": self.metrics["block_history"]
        }
